/*
Ejercicio 7.
Concatenación de arreglos: Crea dos arreglos de strings y luego concaténalos en un solo arreglo.
*/

let arreglo1 = ["Hola", "Mundo"];
let arreglo2 = ["Cómo", "Estás"];

let arregloConcatenado = arreglo1.concat(arreglo2);

console.log("Arreglo 1: " + arreglo1);
console.log("Arreglo 2: " + arreglo2);
console.log("Arreglo concatenado: " + arregloConcatenado);
